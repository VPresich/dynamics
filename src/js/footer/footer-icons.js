const iconFacebookUrlFooter = "https://www.facebook.com/goITclub/";
const iconInstagramUrlFooter = "https://www.instagram.com/goitclub/";
const iconYoutubeUrlFooter = "https://www.youtube.com/c/GoIT";

function createIconMarkupFooter(url, label) {
  return `
    <li class="social-item-footer">
      <a class="social-link-footer" target="_blank" rel="noopener" href="${url}">
        <svg class="social-icon" width="28" height="28" aria-label="${label} icon">
          <use href="../img/icons/symbols.svg#${label.toLowerCase()}"></use>
        </svg>
      </a>
    </li>
  `;
}

const socialListFooter = document.querySelector('.social-list');
// const ulElement = document.querySelector('.social-list-header');

const iconsMarkup = `
  ${createIconMarkupFooter(iconFacebookUrlFooter, "Facebook")}
  ${createIconMarkupFooter(iconInstagramUrlFooter, "Instagram")}
  ${createIconMarkupFooter(iconYoutubeUrlFooter, "Youtube")}
`;

socialListFooter.insertAdjacentHTML('beforeend', iconsMarkup);